package org.madridjs.logopoll.exceptions;

public class NotVotesFoundException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public NotVotesFoundException() {
		super();
		
	}

	public NotVotesFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public NotVotesFoundException(String arg0) {
		super(arg0);
	}

	public NotVotesFoundException(Throwable arg0) {
		super(arg0);
		
	}
	
	

}
